#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : KMToMeter
// Description : It is used to accept distance in kilometre and convert it into meter
// Input : Integer
// Output : Integer
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

double KMToMeter(int iNo)
{
    int iMeter = 0;

    iMeter = iNo * 1000;

    return iMeter;
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter distance: ");
    scanf("%d", &iValue);

    iRet = KMToMeter(iValue);

    printf("Distance in Meter is %d", iRet);

    return 0;
}