#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : FhToCs
// Description : It is used to Accept temperature in fahrenheit & convert it to celsius 
// Input : Float
// Output : Double
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

double FhToCs(float fFahrenheit)
{
    double dCelsius = 0.0f;

    dCelsius = ((fFahrenheit - 32) * (5.0/9.0));

    return dCelsius;
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit: ");
    scanf("%f", &fValue);

    dRet = FhToCs(fValue);

    printf("Temperature in Celsius is %f", dRet);

    return 0;
}