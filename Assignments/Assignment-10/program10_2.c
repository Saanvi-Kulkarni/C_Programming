#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : RectangleArea
// Description : It is used to accept width & height of rectangle from user & calculate its area
// Input : Float
// Output : Double
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

double RectangleArea(float fHeight, float fWidth)
{
    double dArea = 0.0;

    dArea = fHeight * fWidth;

    return dArea;
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue1 = 0.0f, fValue2 = 0.0f;
    double dRet = 0.0;

    printf("Enter Height: ");
    scanf("%f", &fValue1);

    printf("Enter Width: ");
    scanf("%f", &fValue2);

    dRet = RectangleArea(fValue1, fValue2);

    printf("Area of Rectangle is %f", dRet);

    return 0;
}