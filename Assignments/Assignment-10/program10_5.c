#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : SquareMeter
// Description : It is used to accept area in square feet & convert it into square meter
// Input : Integer
// Output : Double
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iSqfeet)
{
    double dSqmeter = 0.0;

    dSqmeter = (iSqfeet * 0.0929);

    return dSqmeter;
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet: ");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);

    printf("Area in Square Meter is %f", dRet);

    return 0;
}