#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : CheckNumberType
// Description : It is used to Accept a number from user, if no is < 50 then it is small,
//               if it is > 50 & < 100 then it is medium, and if if is > 100 then it is big
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void CheckNumberType(int iNo)
{
    if(iNo <= 50)
    {
        printf("Small");
    }
    else if((iNo > 50) && (iNo < 100))
    {
        printf("Medium");
    }
    else
    {
        printf("Large");
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number: ");
    scanf("%d", &iValue);

    CheckNumberType(iValue);

    return 0;
}