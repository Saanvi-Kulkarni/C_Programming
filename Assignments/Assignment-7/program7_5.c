#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : MultipleDisplay
// Description : It is used to Accepts N from user and print first 5 multiples of N 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
    int iCnt = 0;
    int iMultiple = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        iMultiple = iNo * iCnt;
        printf("%d\n", iMultiple);
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a number: ");
    scanf("%d", &iValue);

    MultipleDisplay(iValue);

    return 0;
}