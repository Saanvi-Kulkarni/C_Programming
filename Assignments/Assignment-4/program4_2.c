#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : FactRev
// Description : It is used to Accept a no from user & display its factors in descending order
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void FactRev(int iNo)
{
    int iCnt = 0;

    for(iCnt = (iNo/2); iCnt >= 1; iCnt--)
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\n", iCnt);
        }

    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter a number: ");
    scanf("%d", &iValue);

    FactRev(iValue);

    return 0;
}
