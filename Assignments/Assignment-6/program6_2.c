#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : CheckGreater
// Description : It is used to Accept a number from user & check if it is greater than 100 
// Input : Integer
// Output : Boolean
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

bool CheckGreater(int iNo)
{
    if (iNo > 100)
    {
        return true;
    }
    else
    {
        return false;
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Enter a number: ");
    scanf("%d", &iValue);

    bRet = CheckGreater(iValue);

    if(bRet == true)
    {
        printf("Greater");
    }
    else
    {
        printf("Smaller");
    }

    return 0;
}