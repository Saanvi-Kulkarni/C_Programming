////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : SumNumbers
// Description : It is used to find sum of first N natural nos
// Input : Integer
// Output : Integer
// Author : Saanvi Anand Kulkarni
// Date : 23/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

int SumNumbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iSum = iSum + iCnt;

        //printf("%d\n", iCnt);
    }

    return iSum;
}

// Time Complexity: O(N)

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter limit: ");
    scanf("%d", &iValue);

    iRet = SumNumbers(iValue);

    printf("Sum of first N natural numbers is: %d", iRet);

    return 0;
}