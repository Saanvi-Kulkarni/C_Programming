////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : PrintEvenNumbers
// Description : It is used to Accept N and print all even nos till N 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 23/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void PrintEvenNumbers(int iNo)
{
    int iCnt = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if((iCnt % 2) == 0)
        {
            printf("%d\n", iCnt);
        }
    }
}

// Time Complexity: O(N)

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter limit: ");
    scanf("%d", &iValue);

    PrintEvenNumbers(iValue);

    return 0;
}