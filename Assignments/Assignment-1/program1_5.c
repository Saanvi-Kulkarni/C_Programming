#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////
//
// Function Name : Accept
// Description : It is used to Accept a number and print that number of stars  
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*");
    }
}

////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : \n");
    scanf("%d", &iValue);

    Accept(iValue);

    return 0;
}