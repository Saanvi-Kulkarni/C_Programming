#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description : Accept a no. from user & if no < 10 print 'Hello' otherwise, print 'Demo'
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a number :");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}