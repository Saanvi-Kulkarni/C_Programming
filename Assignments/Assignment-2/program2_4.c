void Display(int iNo, int iFrequency)
{
    int i = 0;

    if(iFrequency < 0)
    {
        iFrequency = -iFrequency;
    }

    for(i = 1; i <= iFrequency; i++)
    {
        printf("%d\n", iNo);
    }


}

