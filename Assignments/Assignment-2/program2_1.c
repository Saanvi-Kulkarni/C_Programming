#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description : It is used to Accept a number from user & print that number of * 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    if (iNo < 0)
    {
        iNo = -iNo;
    }

    while(iCnt < iNo)
    {
        printf("*\t");
        iCnt++;
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a number :");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}