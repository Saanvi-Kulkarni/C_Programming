#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : DollarToINR
// Description : It is used to Accept the amount in US dollar & return its value in Indian Currency 
// Input : Integer
// Output : Integer
// Author : Saanvi Anand Kulkarni
// Date : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

int DollarToINR(int iNo)
{
    int iRupee = 0;

    iRupee = 70 * iNo;

    return iRupee;
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number of USD Currency: ");
    scanf("%d", &iValue);

    iRet = DollarToINR(iValue);

    printf("Value in INR is %d", iRet);

    return 0;
}