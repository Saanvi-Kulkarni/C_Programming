#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : is_divisible_by_five
// Description : It is used to Accept a number from user & check of divisible by 5 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 23/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

bool is_divisible_by_five(int iNo)
{
    if((iNo % 5) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }

}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main(void)
{
    int iValue;

    printf("Enter a number: ");
    scanf("%d", &iValue);

    printf("%s\n", is_divisible_by_five(iValue) ? "Yes" : "No");

    return 0;
}