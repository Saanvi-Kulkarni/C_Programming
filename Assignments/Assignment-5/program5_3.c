////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : CheckLeapYear
// Description : It is used to Accept a year and check if it is leap year 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void CheckLeapYear(int year)
{
    if((year % 4) == 0)
    {
        printf("It is a Leap Year.");
    }
    else
    {
        printf("It is not a Leap Year.");
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int yr;

    printf("Enter year: ");
    scanf("%d", &yr);

    CheckLeapYear(yr);

    return 0;
}