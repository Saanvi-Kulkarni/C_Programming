void CheckEvenOdd(int num)
{
    if((num % 2) == 0)
    {
        printf("Number is Even.");
    }
    else
    {
        printf("Number is Odd.");
    }

}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number: ");
    scanf("%d", &number);

    CheckEvenOdd(number);

    return 0;
}