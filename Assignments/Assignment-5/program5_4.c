#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : CheckNumberType
// Description : It is used to Accept a number & Check Positive, Negative or Zero 
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void CheckNumberType(int iNo)
{
    if(iNo > 0)
    {
        printf("Positive");
    }
    else if(iNo < 0)
    {
        printf("Negative");
    }
    else
    {
        printf("Zero");
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number: ");
    scanf("%d", &iValue);

    CheckNumberType(iValue);

    return 0;
}