////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : DisplayFactor
// Description : It is used to Accept one no from user and print even factors of that no  
// Input : Integer
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void DisplayFactor(int iNo)
{
    int i = 0;

    if (iNo <= 0)
    {
        iNo = -iNo;
    }

    for(i = 1; i <= (iNo/2); i++)
    {
        if (((iNo % i) == 0) && ((i % 2) == 0))
        {
            printf("%d\t",i);
        }
    }

}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : \t");
    scanf("%d", &iValue);

    DisplayFactor(iValue);

    return 0;
}