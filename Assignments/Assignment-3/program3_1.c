////////////////////////////////////////////////////////////////////////////////////////

void PrintEven(int iNo)
{
    if (iNo <= 0)
    {
        return;
    }

    int iCnt = 0; 
    int iFrequency = 0;
    iFrequency = 2 * iNo;

    for(iCnt = 2; iCnt <= iFrequency; iCnt += 2)
    {
        printf("%d\t", iCnt);
    }

}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : \t");
    scanf("%d", &iValue);

    PrintEven(iValue);

    return 0;
}