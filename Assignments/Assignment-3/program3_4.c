#include<stdio.h>
#include <ctype.h>

////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : DisplayConvert
// Description : It is used to Accept a character from the user & convert case of that character
// Input : Character
// Output : NA
// Author : Saanvi Anand Kulkarni
// Date : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void DisplayConvert(char cValue)
{
    if(isupper(cValue))
    {
        printf("%c", tolower(cValue));
    }
    else
    {
        printf("%c", toupper(cValue));
    }
}

////////////////////////////////////////////////////////////////////////////////////////
//
// Entry Point Function of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter character : \t");
    scanf("%c", &cValue);

    DisplayConvert(cValue);

    return 0;
}